package HesapMakinası;

public class DortIslem {
	public int Toplama(int Sayı1,int Sayı2) {
		
		return Sayı1+Sayı2;
	}
public int Cıkarma(int Sayı1,int Sayı2) {
		
		return Sayı1-Sayı2;
	}
public int carpma(int Sayı1,int Sayı2) {
	
	return Sayı1*Sayı2;
	}
public int Bolme(int Sayı1,int Sayı2) {
	
	return Sayı1/Sayı2;
	}
}
